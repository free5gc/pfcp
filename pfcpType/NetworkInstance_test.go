func TestMarshalNetworkInstance(t *testing.T) {
	t.Parallel()

	tests := []struct {
		in       NetworkInstance
		expected []byte
	}{
		{
			in: NetworkInstance{
				NetworkInstance: "www.example.com",
			},
			expected: []byte{
				0x77, 0x77, 0x77,
				0x2e, 0x65, 0x78, 0x61, 0x6D, 0x70, 0x6C, 0x65,
				0x2e, 0x63, 0x6F, 0x6D,
			},
		},
		{
			in: NetworkInstance{
				NetworkInstance: "www.example.com",
				FQDNEncoding:    true,
			},
			expected: []byte{
				0x3, 0x77, 0x77, 0x77,
				0x7, 0x65, 0x78, 0x61, 0x6D, 0x70, 0x6C, 0x65,
				0x3, 0x63, 0x6F, 0x6D,
			},
		},
		{
			in: NetworkInstance{
				NetworkInstance: "UPF-a.free5gc.org",
				FQDNEncoding:    true,
			}, // case-insnesitive case
			expected: []byte{
				0x5, 0x55, 0x50, 0x46, 0x2d, 0x61,
				0x7, 0x66, 0x72, 0x65, 0x65, 0x35, 0x67, 0x63,
				0x3, 0x6F, 0x72, 0x67,
			},
		},
		{
			// length of label = 62, length of encoded buffer = 100
			NetworkInstance{
				NetworkInstance: "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789" +
					".ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghij",
				FQDNEncoding: true,
			},
			[]byte{0x3E,
				0x41, 0x42, 0x43, 0x44, 0x45, 0x46, 0x47, 0x48, 0x49,
				0x4A, 0x4B, 0x4C, 0x4D, 0x4E, 0x4F, 0x50, 0x51, 0x52,
				0x53, 0x54, 0x55, 0x56, 0x57, 0x58, 0x59, 0x5A,
				0x61, 0x62, 0x63, 0x64, 0x65, 0x66, 0x67, 0x68, 0x69,
				0x6A, 0x6B, 0x6C, 0x6D, 0x6E, 0x6F, 0x70, 0x71, 0x72,
				0x73, 0x74, 0x75, 0x76, 0x77, 0x78, 0x79, 0x7A,
				0x30, 0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38, 0x39,
				0x24,
				0x41, 0x42, 0x43, 0x44, 0x45, 0x46, 0x47, 0x48, 0x49,
				0x4A, 0x4B, 0x4C, 0x4D, 0x4E, 0x4F, 0x50, 0x51, 0x52,
				0x53, 0x54, 0x55, 0x56, 0x57, 0x58, 0x59, 0x5A,
				0x61, 0x62, 0x63, 0x64, 0x65, 0x66, 0x67, 0x68, 0x69, 0x6A,
			},
		},
	}

	for _, test := range tests {
		buf, err := test.in.MarshalBinary()
		require.NoError(t, err)
		require.Equal(t, test.expected, buf)
	}
}

func TestMarshalInvalidNetworkInstance(t *testing.T) {
	t.Parallel()

	tests := []NetworkInstance{
		{
			// length of label > 62
			NetworkInstance: "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789A",
			FQDNEncoding:    true,
		},
		{
			// length of encoded buffer > 100
			NetworkInstance: "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789." +
				"ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijk",
			FQDNEncoding: true,
		},
	}

	for _, test := range tests {
		buf, err := test.MarshalBinary()
		require.Empty(t, buf)
		require.Error(t, err)
	}
}

func TestUnmarshalNetworkInstance(t *testing.T) {
	t.Parallel()

	tests := []struct {
		in       NetworkInstance
		inputbuf []byte
		expected NetworkInstance
	}{
		{
			in: NetworkInstance{},
			inputbuf: []byte{
				0x77, 0x77, 0x77,
				0x2e, 0x65, 0x78, 0x61, 0x6D, 0x70, 0x6C, 0x65,
				0x2e, 0x63, 0x6F, 0x6D,
			},
			expected: NetworkInstance{
				NetworkInstance: "www.example.com",
			},
		},
		{
			in: NetworkInstance{
				FQDNEncoding: true,
			},
			inputbuf: []byte{
				0x3, 0x77, 0x77, 0x77,
				0x7, 0x65, 0x78, 0x61, 0x6D, 0x70, 0x6C, 0x65,
				0x3, 0x63, 0x6F, 0x6D,
			},
			expected: NetworkInstance{
				NetworkInstance: "www.example.com",
				FQDNEncoding:    true,
			},
		},
		{
			in: NetworkInstance{
				FQDNEncoding: true,
			},
			inputbuf: []byte{
				0x5, 0x55, 0x50, 0x46, 0x2d, 0x61,
				0x7, 0x66, 0x72, 0x65, 0x65, 0x35, 0x67, 0x63,
				0x3, 0x6F, 0x72, 0x67,
			},
			expected: NetworkInstance{
				NetworkInstance: "UPF-a.free5gc.org",
				FQDNEncoding:    true,
			},
		},
	}

	for _, test := range tests {
		err := test.in.UnmarshalBinary(test.inputbuf)
		require.NoError(t, err)
		require.Equal(t, test.expected, test.in)
	}
}
